public class constructor {
    static void constructor(int a, int b){
        System.out.println(a+b);
    }
    static void constructor(int c, int d, int e){
        System.out.println(c+d+e);
    }
    


    public static void main(String[] args) {
        constructor(1,2);
        constructor(3,4,5);
       
    }

}
